-- Private bucket; access is controlled by storage.objects policies below.
insert into storage.buckets (id, name, public, file_size_limit)
values ('post-attachments', 'post-attachments', false, 10485760)
on conflict (id) do update set public = false, file_size_limit = 10485760;

create policy "Users can upload files under their own post path"
on storage.objects for insert to authenticated
with check (
  bucket_id = 'post-attachments'
  and (storage.foldername(name))[1] = auth.uid()::text
  and (storage.foldername(name))[2] ~* '^[0-9a-f]{8}-[0-9a-f]{4}-[1-5][0-9a-f]{3}-[89ab][0-9a-f]{3}-[0-9a-f]{12}$'
  and exists (
    select 1 from public.posts p
    where p.id = ((storage.foldername(name))[2])::uuid and p.author_id = auth.uid()
  )
);

create policy "Users can read files attached to visible posts"
on storage.objects for select to authenticated
using (
  bucket_id = 'post-attachments'
  and exists (
    select 1
    from public.post_attachments a
    join public.posts p on p.id = a.post_id
    where a.storage_path = name and (not p.is_secret or p.author_id = auth.uid())
  )
);

create policy "Authors can delete their uploaded files"
on storage.objects for delete to authenticated
using (
  bucket_id = 'post-attachments'
  and (storage.foldername(name))[1] = auth.uid()::text
);
