create policy "Authenticated users can read profiles"
on public.profiles for select to authenticated
using (true);

create policy "Users can update their own profile"
on public.profiles for update to authenticated
using (id = auth.uid())
with check (id = auth.uid());

create policy "Users can read public posts and their own secret posts"
on public.posts for select to authenticated
using (not is_secret or author_id = auth.uid());

create policy "Users can read attachments for visible posts"
on public.post_attachments for select to authenticated
using (
  exists (
    select 1 from public.posts p
    where p.id = post_id and (not p.is_secret or p.author_id = auth.uid())
  )
);

create policy "Authors can add attachments to their posts"
on public.post_attachments for insert to authenticated
with check (
  exists (select 1 from public.posts p where p.id = post_id and p.author_id = auth.uid())
);

create policy "Authors can delete their post attachments"
on public.post_attachments for delete to authenticated
using (
  exists (select 1 from public.posts p where p.id = post_id and p.author_id = auth.uid())
);

create policy "Authenticated users can read likes"
on public.post_likes for select to authenticated
using (true);

create policy "Users can like visible posts"
on public.post_likes for insert to authenticated
with check (
  user_id = auth.uid()
  and exists (
    select 1 from public.posts p
    where p.id = post_id and (not p.is_secret or p.author_id = auth.uid())
  )
);

create policy "Users can remove their own likes"
on public.post_likes for delete to authenticated
using (user_id = auth.uid());

-- Posts are written only through these RPCs so password hashing/checking cannot be bypassed.
create or replace function public.create_post(
  p_title text,
  p_content text,
  p_password text,
  p_is_secret boolean default false
)
returns uuid
language plpgsql
security definer
set search_path = public, extensions
as $$
declare
  new_id uuid;
  current_name text;
begin
  if auth.uid() is null then raise exception '로그인이 필요합니다.' using errcode = '42501'; end if;
  if char_length(trim(p_title)) not between 1 and 200 then raise exception '제목은 1자 이상 200자 이하여야 합니다.'; end if;
  if char_length(trim(p_content)) < 1 then raise exception '내용을 입력해주세요.'; end if;
  if char_length(p_password) < 4 then raise exception '게시글 비밀번호는 4자 이상이어야 합니다.'; end if;

  select display_name into current_name from public.profiles where id = auth.uid();
  if current_name is null then raise exception '사용자 프로필을 찾을 수 없습니다.'; end if;

  insert into public.posts (author_id, author_name, title, content, password_hash, is_secret)
  values (auth.uid(), current_name, trim(p_title), trim(p_content), crypt(p_password, gen_salt('bf', 10)), coalesce(p_is_secret, false))
  returning id into new_id;
  return new_id;
end;
$$;

create or replace function public.update_post(
  p_post_id uuid,
  p_title text,
  p_content text,
  p_password text,
  p_is_secret boolean
)
returns void
language plpgsql
security definer
set search_path = public, extensions
as $$
begin
  if auth.uid() is null then raise exception '로그인이 필요합니다.' using errcode = '42501'; end if;
  if char_length(trim(p_title)) not between 1 and 200 then raise exception '제목은 1자 이상 200자 이하여야 합니다.'; end if;
  if char_length(trim(p_content)) < 1 then raise exception '내용을 입력해주세요.'; end if;

  update public.posts
  set title = trim(p_title), content = trim(p_content), is_secret = coalesce(p_is_secret, false)
  where id = p_post_id
    and author_id = auth.uid()
    and password_hash = crypt(p_password, password_hash);

  if not found then raise exception '게시글 비밀번호가 틀렸거나 수정 권한이 없습니다.' using errcode = '42501'; end if;
end;
$$;

create or replace function public.delete_post(p_post_id uuid, p_password text)
returns void
language plpgsql
security definer
set search_path = public, extensions
as $$
begin
  if auth.uid() is null then raise exception '로그인이 필요합니다.' using errcode = '42501'; end if;

  delete from public.posts
  where id = p_post_id
    and author_id = auth.uid()
    and password_hash = crypt(p_password, password_hash);

  if not found then raise exception '게시글 비밀번호가 틀렸거나 삭제 권한이 없습니다.' using errcode = '42501'; end if;
end;
$$;

